# Variável de texto
nome = "Ralf"
print(type(nome))

# Variável de número inteiro
idade = 22
print(type(idade))

# Variável de número real
altura = 1.74
print(type(altura))

# Variável contendo números e letras
codigo = 1j
print(type(codigo))

# Variável lógica
estuda = True
print(type(estuda))

# Variável "nula"
variavel = None
print(type(variavel))