# As informações contidas no SET são desordenadas

# Criar um set
frutas = {"Banana", "Maçã", "Abacaxi", "Morango"}

# Cadastrar um novo valor no set
frutas.add("Melão")

# Alterar um valor no set
# Não é possível

# Remover um valor no set
frutas.remove("Abacaxi")

# Exibir os valores contidos no set
print(frutas)